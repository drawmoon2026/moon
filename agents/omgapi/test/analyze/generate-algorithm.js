/**
 * generate-algorithm.js — 汇总反推参数,生成服务器算法 JS(全程不碰答案)
 *
 * 只读 analyze/inferred/*.json(paytable/layout/reel_weights/triggers/cascade),
 * 拼出一个自包含的算法模块 generated/algorithm-74.js:
 *   - 按 reel_weights 加权合成落点(每轴 7 格)
 *   - 按 paytable 在可视格上结算 ways(win = pay × ways × cs × ml)
 *   - 按 cascade 规则做 tumble(消除中奖 → 重力下落 → 补顶)
 *   - 按 triggers 套 wm 阶梯 + 金色变 wild(R2/R3/R4)
 *
 * 用法: node analyze/generate-algorithm.js
 * 产出后: node generated/algorithm-74.js simulate 100000   # 跑 RTP
 */
const fs = require('fs');
const path = require('path');

// 唯一输入:游戏规格(config 即产品,引擎通用)。由 build-config.js 从 inferred/ 合成。
const gid = process.argv[2] || '74';
const spec = JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'config', `pg-${gid}.json`), 'utf8'));
const paytable = spec.paytable.table;
const layout = spec.layout;
const triggers = { wm_table: { base: spec.multiplier.ladder_base, fs: spec.multiplier.ladder_fs },
    gold_wild_reels: spec.gold_wild.reels, gold_to_wild_rate_per_reel: spec.gold_wild.rate_per_reel };

// 从规格里的概率表建每轴采样器([symbol, weight])
function samplers(probByReel) {
    const out = [];
    for (let r = 0; r < 5; r++) {
        const p = probByReel['R' + (r + 1)] || {};
        const entries = Object.entries(p).map(([s, prob]) => [+s, prob]);
        const total = entries.reduce((a, [, w]) => a + w, 0) || 1;
        out.push({ entries, total });
    }
    return out;
}
const baseSampler = samplers(spec.reels.base);
const fsSampler = spec.reels.free_spin && Object.keys(spec.reels.free_spin).length ? samplers(spec.reels.free_spin) : baseSampler;

const PARAMS = {
    paytable, reels: layout.reels, visible: layout.visible, rowsPerReel: layout.rows_per_reel,
    wmBase: triggers.wm_table.base, wmFs: triggers.wm_table.fs,
    goldWildReels: triggers.gold_wild_reels, goldWildRate: triggers.gold_to_wild_rate_per_reel,
    baseSampler, fsSampler,
    // RTP 校准(target-ratio 层,契约 rtp_config.no_win_prob):独立抽样中奖率过高,
    // 用 noWinProb 强制一部分 spin 无奖,把 RTP 压到目标(庄家可调)。calibrate() 自动求解。
    noWinProb: 0,
    targetRtp: config74rtp(),
};
function config74rtp() {
    try { return JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'env', 'mock', '74', 'config.json'), 'utf8')).rtp / 100; }
    catch { return 0.9681; }
}

// ── 生成的算法引擎源码(模板)──
const ENGINE = `
// === generated by analyze/generate-algorithm.js —— 勿手改,改反推脚本后重生成 ===
// 参数全部来自 spin 样本反推(answer-free)。RTP 由权重决定,可再叠 target-ratio 调节。
const P = ${JSON.stringify(PARAMS)};
const WILD = 0;

function draw(sampler) {
  let x = Math.random() * sampler.total, i = 0;
  for (const [sym, c] of sampler.entries) { if ((x -= c) < 0) return sym; i++; }
  return sampler.entries[sampler.entries.length - 1][0];
}

// 合成一整块 35 格落点(sampler 是按轴的数组)
function spinBoard(sampler) {
  const b = new Array(35).fill(0);
  for (let r = 0; r < 5; r++) { const [s, e] = P.reels[r]; for (let i = s; i <= e; i++) b[i] = draw(sampler[r]); }
  return b;
}

// ways 结算:可视格上,从第0轴起连续含该符号(或wild)的轴数=N,ways=各轴出现数乘积
// lw(每符号赢分)= pay×ways×cs×ml,不含 wm;wm 只乘在这一步的总分上(tw = Σlw × wm)
function settle(board, cs, ml, wm) {
  const wins = {}; const winCells = new Set(); let base = 0;
  for (let sym = 2; sym <= 10; sym++) {
    const per = []; const cellsByReel = [];
    for (let r = 0; r < 5; r++) {
      let cnt = 0; const cells = [];
      for (const idx of P.visible[r]) { const v = board[idx]; if (v === sym || v === WILD) { cnt++; cells.push(idx); } }
      per.push(cnt); cellsByReel.push(cells);
    }
    let N = 0; for (let r = 0; r < 5; r++) { if (per[r] > 0) N++; else break; }
    const pay = P.paytable[sym] && P.paytable[sym][N];
    if (N >= 3 && pay) {
      const ways = per.slice(0, N).reduce((a, b) => a * b, 1);
      const lw = pay * ways * cs * ml;              // 每符号赢分,不含 wm
      wins[sym] = lw; base += lw;
      for (let r = 0; r < N; r++) for (const idx of cellsByReel[r]) winCells.add(idx);
    }
  }
  return { wins, winCells: [...winCells], total: base * wm };   // 总分 = Σlw × wm
}

// 级联:消除中奖格 → 重力下落(高idx=底)→ 补顶
function tumble(board, remove, sampler) {
  const rm = new Set(remove); const nb = new Array(35).fill(0);
  for (let r = 0; r < 5; r++) {
    const [s, e] = P.reels[r]; const kept = [];
    for (let i = s; i <= e; i++) if (!rm.has(i)) kept.push(board[i]);
    const newCount = (e - s + 1) - kept.length; const col = [];
    for (let k = 0; k < newCount; k++) col.push(draw(sampler[r]));  // 补顶用合成(真实为PG条带)
    col.push(...kept);
    for (let o = 0; o < col.length; o++) nb[s + o] = col[o];
  }
  return nb;
}

// 金色变 wild:R2/R3/R4 可视格按反推概率原地变 wild
function applyGoldWild(board) {
  for (let r = 0; r < 5; r++) {
    const rate = P.goldWildRate['R' + (r + 1)] || 0;
    if (rate <= 0) continue;
    for (const idx of P.visible[r]) if (board[idx] !== WILD && Math.random() < rate) board[idx] = WILD;
  }
}

// 一次完整下注:base 局(含级联链),返回总赢分
function spin(cs = 0.02, ml = 10, freeSpin = false) {
  if (!freeSpin && Math.random() < P.noWinProb) return { total: 0, steps: [] };  // target-ratio 无奖注
  const sampler = freeSpin ? P.fsSampler : P.baseSampler;
  const wmTable = freeSpin ? P.wmFs : P.wmBase;
  let board = spinBoard(sampler);
  applyGoldWild(board);
  let total = 0, mi = 0, steps = [];
  while (true) {
    const wm = wmTable[Math.min(mi, wmTable.length - 1)];
    const { wins, winCells, total: stepWin } = settle(board, cs, ml, wm);
    steps.push({ board: board.slice(), wins, win: stepWin, wm });
    if (stepWin <= 0 || winCells.length === 0) break;
    total += stepWin;
    board = tumble(board, winCells, sampler);
    applyGoldWild(board);
    mi++;
  }
  return { total, steps };
}

function simulate(n, cs = 0.02, ml = 10) {
  let bet = 0, won = 0, wins = 0, maxWin = 0;
  for (let i = 0; i < n; i++) { const r = spin(cs, ml); bet += cs * ml; won += r.total; if (r.total > 0) wins++; maxWin = Math.max(maxWin, r.total); }
  return { spins: n, rtp: won / bet, hitRate: wins / n, maxWinX: maxWin / (cs * ml) };
}

// 校准 noWinProb,使 RTP 命中目标(target-ratio 层,二分求解)
function calibrate(target = P.targetRtp, n = 60000) {
  P.noWinProb = 0;
  const raw = simulate(n).rtp;                 // 未压制的原始 RTP
  if (raw <= target) { P.noWinProb = 0; return { noWinProb: 0, rtp: raw, note: '原始RTP已≤目标,无需压制' }; }
  P.noWinProb = 1 - target / raw;              // 无奖注按比例压 RTP:rtp' ≈ raw × (1-p)
  const rtp = simulate(n).rtp;
  return { noWinProb: +P.noWinProb.toFixed(4), rtp, target };
}

module.exports = { spin, simulate, settle, tumble, calibrate, PARAMS: P };

if (require.main === module) {
  const cmd = process.argv[2];
  if (cmd === 'simulate') {
    const s = simulate(parseInt(process.argv[3] || '100000', 10));
    console.log('模拟 ' + s.spins + ' 次: RTP=' + (s.rtp * 100).toFixed(2) + '%  命中率=' + (s.hitRate * 100).toFixed(1) + '%  最大赢=' + s.maxWinX.toFixed(1) + 'x');
  } else if (cmd === 'calibrate') {
    const c = calibrate(process.argv[3] ? +process.argv[3] : P.targetRtp);
    console.log('校准: noWinProb=' + c.noWinProb + ' → RTP=' + (c.rtp * 100).toFixed(2) + '% (目标 ' + (P.targetRtp * 100).toFixed(2) + '%)');
    const s = simulate(100000);
    console.log('校准后 10万次: RTP=' + (s.rtp * 100).toFixed(2) + '%  命中率=' + (s.hitRate * 100).toFixed(1) + '%');
  } else {
    console.log('用法: node algorithm-74.js simulate [n] | calibrate [targetRtp]');
  }
}
`;

const OUT_DIR = path.join(__dirname, '..', 'generated');
fs.mkdirSync(OUT_DIR, { recursive: true });
const outFile = path.join(OUT_DIR, 'algorithm-74.js');
fs.writeFileSync(outFile, ENGINE.trimStart());

console.log('✅ 已生成服务器算法 → generated/algorithm-74.js');
console.log('   输入(反推参数,answer-free):paytable / layout / reel_weights / triggers / cascade');
console.log('   赔表符号数:', Object.keys(paytable).length, '| 可视格:', layout.visible.map(v => v.length).join('/'),
    '| wm base', JSON.stringify(triggers.wm_table.base), 'fs', JSON.stringify(triggers.wm_table.fs));
console.log('\n跑一遍验证: node generated/algorithm-74.js simulate 100000');
